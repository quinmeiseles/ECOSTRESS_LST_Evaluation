#!/usr/bin/env python3
"""
ECOSTRESS Water Mask to Land Polygons

This script converts one or more ECOSTRESS Water Mask GeoTIFFs into a unified
set of land polygons clipped to an area of interest (AOI). It automatically
handles multiple input tiles from different UTM zones and reprojects everything
to EPSG:4326 (WGS84 latitude/longitude).

Folder-based workflow:
  1. Put this script in the same folder as:
       - one AOI shapefile
       - one or more ECOSTRESS water mask GeoTIFFs
  2. Run the script from that folder.
  3. Outputs are saved back into the same folder.

Inputs discovered automatically:
  - AOI shapefile:
      The script searches the working folder for a .shp file.
      Generated output shapefiles from previous runs are ignored.
      If multiple candidate AOI shapefiles are found, the script prefers a
      single file with "aoi" in the name. Otherwise, it asks you to remove or
      rename extras.

  - Water mask GeoTIFFs:
      The script searches the working folder for .tif/.tiff files.
      Generated output TIFFs from previous runs are ignored.

Outputs, all saved in the working folder:
  - binary_summed.tif           merged binary water mask
  - binary_summed_clipped.tif   merged binary water mask clipped to AOI
  - rigid.shp                   unsmoothed land polygons
  - smooth.shp                  lightly smoothed land polygons
  - smoother.shp                moderately smoothed land polygons
  - smoothest.shp               most smoothed land polygons
"""

# Import the Libraries Needed for Polygonization
from pathlib import Path

import numpy as np
import rasterio
from rasterio.warp import (
    calculate_default_transform,
    reproject,
    transform_bounds,
    transform_geom,
    Resampling,
)
from rasterio.mask import mask
from rasterio.features import shapes
from rasterio.crs import CRS
import fiona
from shapely.geometry import shape, mapping


# ---------------------------------------------------------------------------
# CONFIGURATION
# ---------------------------------------------------------------------------

# Generated files that should NOT be treated as new inputs if the script is rerun.
GENERATED_TIFF_NAMES = {
    "binary_summed.tif",
    "binary_summed_clipped.tif",
}

GENERATED_SHP_NAMES = {
    "rigid.shp",
    "smooth.shp",
    "smoother.shp",
    "smoothest.shp",
}


# ---------------------------------------------------------------------------
# DISCOVERY HELPERS
# ---------------------------------------------------------------------------

def get_working_folder() -> Path:
    """
    Return the folder that contains this script.

    This makes the workflow portable: users can place the script, AOI shapefile,
    and water-mask TIFFs in one folder and run the script directly from there.

    If __file__ is unavailable, fall back to the current working directory.
    """
    try:
        return Path(__file__).resolve().parent
    except NameError:
        return Path.cwd().resolve()


def discover_aoi_shapefile(folder: Path) -> Path:
    """
    Find the AOI shapefile in the working folder.

    Ignores shapefiles generated by this script. If multiple candidate shapefiles
    exist, a single file with "aoi" in the filename is preferred.
    """
    shp_candidates = sorted(
        p for p in folder.iterdir()
        if p.is_file()
        and p.suffix.lower() == ".shp"
        and p.name.lower() not in GENERATED_SHP_NAMES
    )

    if not shp_candidates:
        raise FileNotFoundError(
            f"No AOI shapefile found in:\n  {folder}\n\n"
            "Place exactly one AOI .shp file in the same folder as this script."
        )

    if len(shp_candidates) == 1:
        return shp_candidates[0]

    aoi_named = [p for p in shp_candidates if "aoi" in p.stem.lower()]
    if len(aoi_named) == 1:
        return aoi_named[0]

    candidate_list = "\n".join(f"  - {p.name}" for p in shp_candidates)
    raise RuntimeError(
        "Multiple candidate AOI shapefiles were found:\n"
        f"{candidate_list}\n\n"
        "Keep only one AOI .shp in the folder, or rename the intended AOI file "
        "so its filename contains 'AOI'."
    )


def discover_water_tiffs(folder: Path) -> list[Path]:
    """
    Find water mask GeoTIFFs in the working folder.

    Ignores TIFF files generated by this script so reruns remain clean.
    """
    tiff_candidates = sorted(
        p for p in folder.iterdir()
        if p.is_file()
        and p.suffix.lower() in {".tif", ".tiff"}
        and p.name.lower() not in GENERATED_TIFF_NAMES
    )

    if not tiff_candidates:
        raise FileNotFoundError(
            f"No input water-mask GeoTIFFs found in:\n  {folder}\n\n"
            "Place one or more water-mask .tif or .tiff files in the same folder "
            "as this script."
        )

    return tiff_candidates


def read_aoi_geometries_as_crs(aoi_shp: Path, dst_crs: str) -> list[dict]:
    """
    Read AOI geometries and transform them to dst_crs if needed.

    The output raster is created in EPSG:4326, so this makes clipping safer even
    if the AOI shapefile is not already in EPSG:4326.
    """
    with fiona.open(aoi_shp, "r") as shp:
        src_crs = shp.crs_wkt or shp.crs
        geometries = [feat["geometry"] for feat in shp if feat.get("geometry")]

    if not geometries:
        raise RuntimeError(f"The AOI shapefile contains no valid geometries: {aoi_shp}")

    if not src_crs:
        print(
            "[WARNING] AOI shapefile has no CRS metadata. "
            f"Assuming it is already in {dst_crs}."
        )
        return geometries

    try:
        src_crs_obj = CRS.from_user_input(src_crs)
        dst_crs_obj = CRS.from_user_input(dst_crs)
    except Exception as exc:
        print(
            "[WARNING] Could not parse AOI CRS metadata. "
            f"Assuming AOI is already in {dst_crs}. Details: {exc}"
        )
        return geometries

    if src_crs_obj == dst_crs_obj:
        return geometries

    print(f"[INFO] Reprojecting AOI geometries from {src_crs_obj} to {dst_crs_obj}.")
    return [
        transform_geom(src_crs_obj, dst_crs_obj, geom)
        for geom in geometries
    ]


# ---------------------------------------------------------------------------
# MAIN WORKFLOW
# ---------------------------------------------------------------------------

def main():
    print("\n=== ECOSTRESS Water Mask → Land Polygon Workflow ===\n")

    input_folder = get_working_folder()
    print(f"[INFO] Working folder: {input_folder}")

    try:
        aoi_shp = discover_aoi_shapefile(input_folder)
        tiff_files = discover_water_tiffs(input_folder)
    except Exception as exc:
        print(f"\nERROR: {exc}\n")
        return

    print(f"[INFO] AOI shapefile: {aoi_shp.name}")
    print(f"[INFO] Found {len(tiff_files)} candidate water-mask TIFF(s):")
    for tif in tiff_files:
        print(f"   - {tif.name}")

    dst_crs = "EPSG:4326"
    valid_files = []
    all_bounds = []

    print("\n[INFO] Checking raster validity and computing combined bounds...")

    # Step 1: Compute overall bounding box across all valid rasters
    for tif in tiff_files:
        try:
            with rasterio.open(tif) as src:
                if src.crs is None:
                    print(f"[WARNING] Skipping {tif.name}: raster has no CRS.")
                    continue

                b = transform_bounds(src.crs, dst_crs, *src.bounds)
                all_bounds.append(b)
                valid_files.append(tif)
        except Exception as e:
            print(f"[WARNING] Skipping {tif.name}: {e}")

    if not all_bounds:
        print("\nERROR: No valid GeoTIFFs found.")
        return

    # Step 2: Compute bounding box union
    minx = min(b[0] for b in all_bounds)
    miny = min(b[1] for b in all_bounds)
    maxx = max(b[2] for b in all_bounds)
    maxy = max(b[3] for b in all_bounds)

    # Step 3: Define output grid parameters from first valid file
    with rasterio.open(valid_files[0]) as ref:
        ref_transform, _, _ = calculate_default_transform(
            ref.crs, dst_crs, ref.width, ref.height, *ref.bounds
        )
        px_x = abs(ref_transform.a)
        px_y = abs(ref_transform.e)

    width = max(1, int(np.ceil((maxx - minx) / px_x)))
    height = max(1, int(np.ceil((maxy - miny) / px_y)))
    transform = rasterio.transform.from_bounds(minx, miny, maxx, maxy, width, height)

    out_meta = {
        "driver": "GTiff",
        "height": height,
        "width": width,
        "count": 1,
        "dtype": rasterio.uint8,
        "crs": dst_crs,
        "transform": transform,
    }

    # Step 4: Reproject and combine all TIFFs
    print(f"\n[INFO] Merging {len(valid_files)} ECOSTRESS tile(s) into one EPSG:4326 grid...")
    data_sum = np.zeros((height, width), dtype=np.uint32)

    for tif in valid_files:
        with rasterio.open(tif) as src:
            arr = src.read(1)
            reprojected = np.zeros((height, width), dtype=np.uint32)

            reproject(
                source=arr,
                destination=reprojected,
                src_transform=src.transform,
                src_crs=src.crs,
                dst_transform=transform,
                dst_crs=dst_crs,
                resampling=Resampling.nearest,
            )

            data_sum += (reprojected != 0).astype(np.uint32)
            crs_label = src.crs.to_string() if src.crs else "unknown CRS"
            print(f"   → Integrated {tif.name} ({crs_label})")

    # Step 5: Threshold and save binary_summed.tif
    binary = (data_sum >= 1).astype(np.uint8)
    binary_path = input_folder / "binary_summed.tif"

    with rasterio.open(binary_path, "w", **out_meta) as dst:
        dst.write(binary, 1)

    print(f"\n[INFO] Saved merged binary mask: {binary_path.name}")

    # Step 6: Clip to AOI
    try:
        shapes_geom = read_aoi_geometries_as_crs(aoi_shp, dst_crs)
    except Exception as exc:
        print(f"\nERROR: Failed to read AOI shapefile: {exc}")
        return

    with rasterio.open(binary_path) as src:
        try:
            clipped_img, clipped_transform = mask(src, shapes_geom, crop=True)
        except ValueError as exc:
            print(
                "\nERROR: AOI does not overlap the merged raster, or the CRS is wrong.\n"
                f"Details: {exc}"
            )
            return

        clipped_meta = src.meta.copy()
        clipped_meta.update({
            "height": clipped_img.shape[1],
            "width": clipped_img.shape[2],
            "transform": clipped_transform,
        })

    clipped_path = input_folder / "binary_summed_clipped.tif"
    with rasterio.open(clipped_path, "w", **clipped_meta) as dst:
        dst.write(clipped_img)

    print(f"[INFO] Saved clipped mask: {clipped_path.name}")

    # Step 7: Polygonize land (0 values = land)
    print("\n[INFO] Polygonizing land areas...")

    with rasterio.open(clipped_path) as src:
        arr = src.read(1)
        clipped_raster_transform = src.transform
        crs = src.crs

    land_mask = arr == 0
    raw_polys = [
        shape(geom)
        for geom, val in shapes(arr, mask=land_mask, transform=clipped_raster_transform)
        if val == 0
    ]

    if not raw_polys:
        print("[WARNING] No land polygons were generated. Check water mask values and AOI.")
        return

    schema = {"geometry": "Polygon", "properties": {}}
    rigid_path = input_folder / "rigid.shp"

    with fiona.open(rigid_path, "w", driver="ESRI Shapefile", crs=crs, schema=schema) as dst:
        for poly in raw_polys:
            if not poly.is_valid:
                poly = poly.buffer(0)

            if poly.geom_type == "Polygon":
                dst.write({"geometry": mapping(poly), "properties": {}})
            elif poly.geom_type == "MultiPolygon":
                for part in poly.geoms:
                    dst.write({"geometry": mapping(part), "properties": {}})

    print("[INFO] Saved rigid land polygons: rigid.shp")

    # Step 8: Generate smoothed polygon variants
    base_px = max(
        1e-8,
        min(abs(clipped_raster_transform.a) or 0, abs(clipped_raster_transform.e) or 0),
    )

    smoothing_specs = [
        ("smooth.shp", 0.5),
        ("smoother.shp", 0.75),
        ("smoothest.shp", 1.0),
    ]

    for filename, factor in smoothing_specs:
        dist = base_px * factor
        smooth_path = input_folder / filename

        with fiona.open(smooth_path, "w", driver="ESRI Shapefile", crs=crs, schema=schema) as dst:
            for poly in raw_polys:
                smoothed = poly.buffer(-dist).buffer(dist)
                final = smoothed if not smoothed.is_empty else poly

                if not final.is_valid:
                    final = final.buffer(0)

                if final.geom_type == "Polygon":
                    dst.write({"geometry": mapping(final), "properties": {}})
                elif final.geom_type == "MultiPolygon":
                    for part in final.geoms:
                        dst.write({"geometry": mapping(part), "properties": {}})

        print(f"[INFO] Saved {filename}")

    # Step 9: Done
    print("\n[DONE] Processing complete. Outputs saved in:")
    print(f"   {input_folder}\n")
    print("   • binary_summed.tif")
    print("   • binary_summed_clipped.tif")
    print("   • rigid.shp")
    print("   • smooth.shp")
    print("   • smoother.shp")
    print("   • smoothest.shp\n")


# RUN SCRIPT
if __name__ == "__main__":
    main()
